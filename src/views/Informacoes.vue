<h4 class="mt-1 mb-2">O que é Setembro Amarelo?</h4>

<p>O setembro amarelo é uma campanha de conscientização da população ao redor do tema do suicídio, prática geralmente associada a depressão. É divulgada pela <strong>Associação Brasileira de Psiquiatria (ABP)</strong>, pelo <strong> Conselho Federal de Medicina (CFM)</strong> e pelo <strong> Centro de Valorização da Vida (CVV)</strong>. As instituições também contam com a ajuda na divulgação tanto de empresas como de pessoas individualmente.
O mês de setembro foi escolhido porque é no dia 10 de setembro que se comemora o Dia Mundial de Prevenção ao Suicídio.</p>

<h4 class="mt-1 mb-2">Quando surgiu o Setembro Amarelo?</h4>

<p>A data foi estabelecida em 2015, e desde lá virou uma campanha amplamente divulgada em todo o país.

Atualmente grande parte das capitais adere à campanha. Nesses casos, muitos monumentos e pontos turísticos da cidade ficam iluminados da cor amarela em apoio.</p>

