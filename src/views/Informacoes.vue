<h4 class="mt-4 mb-4">Objetivos do Setembro Amarelo
</h4>
<p>O principal objetivo do setembro amarelo é trazer visibilidade para o tema, e conscientizar a população para a prevenção de suicídios.

Os veículos de mídia, a internet e as redes sociais tem conseguido trazer cada vez mais visibilidade para esse tema, que ainda é tratado como tabu em muitas regiões do país.

A campanha visa trazer informação para a população, disponibilzando diversos materiais gratuitos pela internet, entrevistas para canais de comunicação. Esses materiais tratam desde dados sobre o suicídio até as formas de como abordar esse tema e identificar pessoas que precisam de ajuda.</p>

